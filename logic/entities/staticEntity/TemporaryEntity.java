package saga.progetto.metodologie.core.logic.entities.staticEntity;

/**
 * 
 * The class {@code TemporaryEntity} represents an entity that will fade after a fixed span of time.
 *
 */
public class TemporaryEntity extends StaticEntity
{
	public TemporaryEntity(int x, int y)
	{
		setX(x);
		setY(y);
	}
}
